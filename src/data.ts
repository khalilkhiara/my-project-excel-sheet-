import { PipingItem } from "./types";

// Helper to reliably parse raw CSV matching the given schema
export function parseCSV(rawText: string): PipingItem[] {
  if (!rawText || !rawText.trim()) return [];
  
  const lines = rawText.trim().split("\n");
  const headers = lines[0].split(";").map(h => h.trim());
  
  // Detect if headers actually exist (contains words like LINE NO or ISO NO)
  const startIndex = lines[0].toLowerCase().includes("line no") ? 1 : 0;
  
  const parsedItems: PipingItem[] = [];
  
  for (let i = startIndex; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;
    
    const columns = line.split(";").map(c => c.trim());
    if (columns.length < 8) continue; // Skip incomplete paths
    
    const [
      lineNumber = "",
      isoNumber = "",
      revNumber = "0",
      service = "",
      classe = "",
      description = "",
      schClass = "",
      typeOfItems = "",
      npd = "",
      cmdtyCode = "",
      qtyRaw = "0",
      unit = ""
    ] = columns;
    
    // Parse quantity (convert potential French comma decimals to DOT decimals)
    const qtyNormalized = qtyRaw.replace(",", ".").replace(/\s+/g, "");
    const qty = parseFloat(qtyNormalized) || 0;
    
    // Normalize type of item
    let itemType = typeOfItems.toUpperCase().trim();
    if (itemType.includes("PIPE")) itemType = "PIPE";
    else if (itemType.includes("TEE")) itemType = "TEE";
    else if (itemType.includes("REDUCER")) itemType = "REDUCER";
    else if (itemType.includes("ELBOW")) itemType = "ELBOW";
    
    parsedItems.push({
      id: `${lineNumber}-${isoNumber}-${npd}-${cmdtyCode}-${i}-${Math.random().toString(36).substr(2, 4)}`,
      lineNumber,
      isoNumber,
      revNumber,
      service,
      classe,
      description,
      schClass,
      typeOfItems: itemType || "UNKNOWN",
      npd,
      cmdtyCode,
      qty,
      unit: unit || "PC"
    });
  }
  
  return parsedItems;
}

